import math

def calculate_factorial(n):
    """
    Функция для вычисления факториала числа n.
    Использует оптимизированный метод из библиотеки math.
    """
    return math.factorial(n)

def main():
    print("--- Программа для вычисления факториала ---")
    
    user_input = input("Введите положительное целое число: ")
    
    try:
        # Пытаемся преобразовать ввод в целое число
        number = int(user_input)
        
        # Проверка на отрицательное число
        if number < 0:
            print("Ошибка: Факториал определен только для неотрицательных целых чисел.")
        else:
            # Вычисление и вывод результата
            result = calculate_factorial(number)
            print(f"Факториал числа {number} равен: {result}")
            
    except ValueError:
        # Обработка случая, когда введен не числовой текст
        print("Ошибка: Пожалуйста, введите корректное целое число.")

if __name__ == "__main__":
    main()
